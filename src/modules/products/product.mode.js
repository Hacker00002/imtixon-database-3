const { fetch } = require("../../postgres");
// /////////////////////////////////////////////////////////////////////////////////////

const GET_ALL_PRODUCTS = `
    SELECT * FROM products;
`;
// /////////////////////////////////////////////////////////////////////////////////////

const CREATE_PRODUCTS = `
    INSERT INTO products(product_img,product_price,product_title,product_sub_title) VALUES($1,$2,$3,$4)
`;
// /////////////////////////////////////////////////////////////////////////////////////

const UPDATE_PRODUCTS = `
    UPDATE products SET product_img = $1, product_price = $2, product_title = $3, product_sub_title = $4 WHERE id = $5
`;
// /////////////////////////////////////////////////////////////////////////////////////

const SEARCH_BY_TITLE = `
    SELECT * FROM products WHERE product_title LIKE $1%
`;
// /////////////////////////////////////////////////////////////////////////////////////
const UPDATE_CERATED = `
    UPDATE products SET updated_at = CURRENT_TIMESTAMP WHERE id = $1
`;

// /////////////////////////////////////////////////////////////////////////////////////
const getAllProducts = () => fetch(GET_ALL_PRODUCTS);

// /////////////////////////////////////////////////////////////////////////////////////
const updateDate = (id) => fetch(UPDATE_CERATED, id);

// /////////////////////////////////////////////////////////////////////////////////////
const serachByTitle = (product_title) => fetch(SEARCH_BY_TITLE, product_title);

// /////////////////////////////////////////////////////////////////////////////////////
const createProduct = (
    product_img,
    product_price,
    product_title,
    product_sub_title
) =>
    fetch(
        CREATE_PRODUCTS,
        product_img,
        product_price,
        product_title,
        product_sub_title
    );

// /////////////////////////////////////////////////////////////////////////////////////
const updateProducts = (
    id,
    product_img,
    product_price,
    product_title,
    product_sub_title
) =>
    fetch(
        UPDATE_PRODUCTS,
        id,
        product_img,
        product_price,
        product_title,
        product_sub_title
    );

// /////////////////////////////////////////////////////////////////////////////////////
module.exports = {
    getAllProducts,
    serachByTitle,
    createProduct,
    updateProducts,
    updateDate,
};
